#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "Enter the value of a & b: ";
    cin >> a >> b;
    cout << "Addition= " << a + b << endl;
    cout << "Subtraction= " << a - b << endl;
    cout << "Multiplication= " << a * b << endl;
    cout << "Division= " << a / b << endl;
    cout << "Modulo= " << a % b << endl;
    return 0;
}